def t(lang: str, key: str, **kw) -> str:
    entry = STRINGS.get(key)
    if not entry:
        return key
    s = entry.get(lang) or entry[DEFAULT]
    return s.format(**kw) if kw else s


def js_bundle(lang: str) -> dict:
    lang = norm(lang)
    return {k: (v.get(lang) or v[DEFAULT]) for k, v in JS.items()}
